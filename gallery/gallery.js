const appendImages = (slides) => {
  image_gallery.innerHTML = "";
  window.scrollTo(0, 0);

  for (let i = 0; i < slides.length; i++) {
    const image_wrapper = document.createElement("div");
    image_wrapper.classList.add("image-wrapper");
    const image = document.createElement("img");

    image.setAttribute("src", slides[i].imageSrc);
    image.setAttribute("alt", `kominkikrosno__${i + 1}`);

    image_gallery.appendChild(image_wrapper);
    image_wrapper.appendChild(image);
  }
};

const changeNavColor = (e) => {
  e.preventDefault();
  e.target.classList.add("active");

  if (e.target.classList.contains("nav-link-fireplaces")) {
    localStorage.setItem("current-photos", "fireplaces");
    nav_awards.classList.remove("active");
  }

  if (e.target.classList.contains("nav-link-awards")) {
    localStorage.setItem("current-photos", "awards");
    nav_fireplaces.classList.remove("active");
  }
};

const getImageModal = (i, slides, state) => {
  gallery_modal.classList.add(state);
  gallery_modal_image_wrapper.removeChild(
    gallery_modal_image_wrapper.lastChild
  );

  const gallery_modal_image = document.createElement("img");
  gallery_modal_image.classList.add("gallery-modal-image");

  gallery_modal_image.setAttribute("src", slides[i].imageSrc);
  gallery_modal_image.setAttribute("alt", `kominkikrosno`);

  gallery_modal_image_wrapper.appendChild(gallery_modal_image);

  //length = slides.length;
};

const closeModal = (e) => {
  e.preventDefault();
  gallery_modal.classList.remove("active");
};

const nextSlide = (e) => {
  e.preventDefault();
  //let length;

  if (nav_fireplaces.classList.contains("active"))
    length = GalleryData__Fireplaces.length;
  if (nav_awards.classList.contains("active"))
    length = GalleryData__Awards.length;

  const current_image = document.querySelector(".gallery-modal-image");

  const src_string = current_image.getAttribute("src");

  number = +src_string.replace(/^\D+/g, "").replace(".jpg", "");
  number++;

  if (number < 10) number = `0${number}`;
  if (number === length + 1)
    current_image.setAttribute("src", src_string.replace(/[0-9]+/, `01`));
  else
    current_image.setAttribute(
      "src",
      src_string.replace(/[0-9]+/, `${number}`)
    );

  //console.log(current_image);
  //console.log(number);
};

